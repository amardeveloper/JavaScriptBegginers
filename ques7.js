// Use lastIndexOf to determine the position of the last occurrence of a script.

const paragraph = 'The quick brown fox jumps over the lazy dog. If the dog barked, was it really lazy?';

const searchTerm = 'dog';

console.log(`the last present index of "${searchTerm}" is ${paragraph.lastIndexOf(searchTerm)}`)

// expected output: the last present index of "dog" is 52
